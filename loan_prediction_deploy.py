import streamlit as st
import pandas as pd
import joblib

# -------------------------------
# Load Model & Encoders
# -------------------------------
try:
    model = joblib.load("loan_prediction_model.pkl")
    encoders = joblib.load("encoders.pkl")
except Exception as e:
    st.error(f"Loading failed: {e}")
    st.stop()

st.title("Loan Prediction App")
st.subheader("Enter Applicant Details")

# -------------------------------
# User Inputs
# -------------------------------
gender = st.selectbox("Gender", ["Male", "Female"])
married = st.selectbox("Married", ["Yes", "No"])
dependents = st.selectbox("Dependents", ["0", "1", "2", "3+"])
education = st.selectbox("Education", ["Graduate", "Not Graduate"])
self_employed = st.selectbox("Self Employed", ["Yes", "No"])
applicant_income = st.number_input("Applicant Income", min_value=0)
coapplicant_income = st.number_input("Coapplicant Income", min_value=0)
loan_amount = st.number_input("Loan Amount", min_value=0)
loan_term = st.number_input("Loan Amount Term", min_value=0)
credit_history = st.selectbox("Credit History", [1.0, 0.0])
property_area = st.selectbox("Property Area", ["Urban", "Semiurban", "Rural"])

# -------------------------------
# Prediction
# -------------------------------
if st.button("Predict"):

    try:
        # Encode inputs using saved encoders
        gender_val = encoders["Gender"].transform([gender])[0]
        married_val = encoders["Married"].transform([married])[0]
        dependents_val = encoders["Dependents"].transform([dependents])[0]
        education_val = encoders["Education"].transform([education])[0]
        self_employed_val = encoders["Self_Employed"].transform([self_employed])[0]
        property_val = encoders["Property_Area"].transform([property_area])[0]

        # Create dataframe in correct order
        input_data = pd.DataFrame([[ 
            gender_val,
            married_val,
            dependents_val,
            education_val,
            self_employed_val,
            applicant_income,
            coapplicant_income,
            loan_amount,
            loan_term,
            credit_history,
            property_val
        ]], columns=[
            "Gender",
            "Married",
            "Dependents",
            "Education",
            "Self_Employed",
            "ApplicantIncome",
            "CoapplicantIncome",
            "LoanAmount",
            "Loan_Amount_Term",
            "Credit_History",
            "Property_Area"
        ])

        prediction = model.predict(input_data)

        if prediction[0] == 1:
            st.success("Loan Approved ✅")
        else:
            st.error("Loan Not Approved ❌")

    except Exception as e:
        st.error(f"Prediction failed: {e}")
